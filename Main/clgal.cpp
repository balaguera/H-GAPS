int main(int argc, char *argv[]){
  time_t start_all;
  char temp;
  time(&start_all);


  
  if(argc==1){
    usage(argv[0]);
    exit(1);
  }
  while((temp =  getopt(argc, argv, "hap:")) != -1){
    switch(temp){
    case 'h' :
      usage(argv[0]);
      break;
    case 'a':
      author();
      break;
    case 'p':
      message();
      string par_file1 = argv[2];
      Cl_FUNCTIONS cCl(par_file1);
      cCl.get_my_power();          
      message_time(start_all);
      break;
    }
  }
  
  
  
  return 0;
}


